package sys;


public class Content {
	
	private String name;
	private String id;
	private String fun;
	
	public Content(){
		name = "";
		id = "";
		fun = "";
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getFun() {
		return fun;
	}
	public void setFun(String fun) {
		this.fun = fun;
	}

}
